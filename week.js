const day = 'воскресенье'
if (day === 'понедельник') {
    console.log(`вы ввели ${day} - первый день недели`)
}
else if (day === 'вторник') {
    console.log(`вы ввели ${day} - второй день недели`)
}
else if (day === 'среда') {
    console.log(`вы ввели ${day} - третий день недели`)
}
else if (day === 'четверг') {
    console.log(`вы ввели ${day} - четвертый день недели`)
}

else if (day === 'пятница') {
    console.log(`вы ввели ${day} - пятый день недели`)
}

else if (day === 'суббота') {
    console.log(`вы ввели ${day} - шестой день недели`)
}

else if (day === 'воскресенье') {
    console.log(`вы ввели ${day} - седьмой день недели`)
}
else {
    console.log('неверно')
}